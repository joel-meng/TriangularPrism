import Foundation

extension RGBColor {
    static let black: RGBColor = .init(red: 0, green: 0, blue: 0)

    static let white: RGBColor = .init(red: 255, green: 255, blue: 255)

    static let red: RGBColor = .init(red: 255, green: 0, blue: 0)

    static let olive: RGBColor = .init(red: 128, green: 128, blue: 0)

    static let silver: RGBColor = .init(red: 192, green: 192, blue: 192)

    static let purple: RGBColor = .init(red: 128, green: 0, blue: 128)

    static let teal: RGBColor = .init(red: 0, green: 128, blue: 128)
}
